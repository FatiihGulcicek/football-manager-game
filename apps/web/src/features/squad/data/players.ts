function buildSummary(players: SquadPlayer[]): SquadSummary {
  const totalAge = players.reduce((total, player) => total + player.age, 0);

  return {
    totalPlayers: players.length,
    averageAge: players.length > 0 ? (totalAge / players.length).toFixed(1) : '-',
    weeklyWage: 'EUR 615K',
    squadValue: 'EUR 192.0M',
    homegrown: players.filter((player) => player.isHomegrown).length,
    foreignPlayers: players.filter((player) => player.isForeign).length,
    injured: players.filter((player) => player.statuses.includes('injured')).length,
    unavailable: players.filter((player) => player.availability !== 'Available').length
  };
}

